 //***************************************************** 
void Run_P(void)           //4: не читая, комада -получить Р
 {
   __bic_SR_register(GIE);     // запретить прерывания 
   ADC_write (ConR);           // запись в конфигурационный регистр :
   ADC_write (Reg_ADC[0][1]);        // старший байт для канала давления
   ADC_write (Reg_ADC[0][2]);        // младший байт для канала давления
   ADC_write (MR);             // запись в регистр режима :
   ADC_write (CCM);                  // обычное чтение
   ADC_write (Reg_ADC[0][3]);        // младший байт для канала давления
   __bis_SR_register(GIE);      //разрешить прерывания
   
   ADC_Func_Ptr = 6;          // нормальный режим - перейти к Read_P
   if (ADC_P_Count == 0)      // старт цикла давления?
     ADC_Func_Ptr = 3;           //да,  перейти к Read_P_Run_ZP
   if (ADC_P_Count++ == 2)    // такт калибровки полного диапазона?
     ADC_Func_Ptr = 5;           //да, перейти к Read_P_Run_FP   
 }
  
 
