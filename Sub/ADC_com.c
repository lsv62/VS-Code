 //***************************************************** 
 void ADC_Start(void)        //0 запуск АЦП, команда -получить Р
 {
     __bic_SR_register(GIE);     // запретить прерывания 
   ADC_write (ConR);           // запись в конфигурационный регистр :
   ADC_write (Reg_ADC[0][1]);        // старший байт для канала давления
   ADC_write (Reg_ADC[0][2]);        // младший байт для канала давления
   ADC_write (MR);             // запись в регистр режима :
   ADC_write (CCM);                  // обычное чтение
   ADC_write (Reg_ADC[0][3]);        // младший байт для канала давления
   __bis_SR_register(GIE);      //разрешить прерывания
  
   ADC_Func_Ptr = 1;         // перейти к Read_P_Run_T
 }
 
//***************************************************** 
void Read_P_Run_T(void)    //1: прочитать Р, команда -получить Т
 {
  __bic_SR_register(GIE);   // запретить прерывания
  ADC_write (ConR);           // запись в конфигурационный регистр :
   ADC_write (Reg_ADC[1][1]);  // старший байт для канала температуры
   ADC_write (Reg_ADC[1][2]);  // младший байт для канала температуры
   ADC_write (MR);          // запись в регистр режима :
   ADC_write (CCM);            // обычное чтение
   ADC_write (Reg_ADC[1][3]);  // младший байт для канала температуры
  __bis_SR_register(GIE);   //разрешить прерывания
   
   P_int.l=ADC_DR_val.l;      // прочитать Р
   ADC_Func_Ptr = 2;        // перейти к Read_T_Run_P
 }
 
