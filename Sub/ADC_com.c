 //***************************************************** 
void Read_P(void)           //6: прочитать Р
  {
    P_int.l=ADC_DR_val.l;          // прочитать Р
    if (++ADC_P_Count >= Number_P_Points) // цикл давления завершен?
    {                                       // да,
      ADC_P_Count = 0;                      // начать новый цикл давления
      if (ADC_T_Count == 0)                 // начало цикла температуры?
        ADC_Func_Ptr = 7;                      // да, перейти к Read_P_Run_ZT
      else                                     // нет, при этом:
        if (ADC_T_Count == 1)                     // такт калибровки полного диапазона температуры?
          ADC_Func_Ptr = 8;                          //да, перейти к Read_P_Run_FT
         else                                         
            ADC_Func_Ptr = 1;                        //нет, перейти к Read_P_Run_T
    }
  }
  
